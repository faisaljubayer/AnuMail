<h2 style="background:#99FF33;padding:5px" align="left">About AnuMail</h2>
<p>AnuMail gives free web based electronic mail. 
it enables you to acquire new customers and to increase growth opportunities with existing customers. With a wide range of Direct Mail Lists Services, label mailing or mail merge services,
 AnuMail enable you to communicate with customers in a better way. </p>

<p>Aim-:  The main aim of AnuMail is to provide a mailing  services which lets you 
compose mail and send to a respective selected object. You can send documents or images 
(attachments). Also one can search emails with the search technology within the inbox, outbox , drafts. </p>


<p>Objective-: To prepare new mailing system on PHP which provide all the services provide by 
any general mailing services with some extra features such as easy to operate, 
free messaging services are objective to make this project at very huge level but we just learn 
how to make mailing service system we are able in future to work over this project easily and provide 
much better facilities. So here we are showing you dummy project on mailing services prepared with help 
of PHP and some other designing tools.</p>


<p>Vision-: Our vision for this project and training is to build and maintains a customer-oriented culture. 
